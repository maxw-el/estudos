package fundamentos;

public class PrimitivoVsObjeto {

	public static void main(String[] args) {
		
		String s = "texto"; //String é uma classe, "s" é um objeto
		s.toUpperCase(); //esse objeto "s" tem atributos e comportamentos através da notação . (ponto)
		
		int a = 123; // isso é um primitivo, ele não tem a notação . com comportamentos
		
		// E existem os Wrappers, que são os objetos dos tipos primitivos (possuem comportamentos)
	}
}
