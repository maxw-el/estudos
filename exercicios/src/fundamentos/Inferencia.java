package fundamentos;

public class Inferencia {

	public static void main(String[] args) {
		
		/*
		 * Inferência é uma forma de declaração de variável que
		 * você deixa o java supor/inferir qual o tipo de variável
		 * será usada.
		 */
		
		var a = 4.5; //infere que é do tipo double
		var b = 4; // inteiro
		// e assim por diante...
		
	}
}
