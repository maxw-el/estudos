package fundamentos;

public class ConversaoNumeroString {
	
	public static void main(String[] args) {
		
		
		// Converte um objeto do tipo Inteiro para uma String
		Integer num1 = 1000;
		System.out.println(num1.toString());
		
		// Converte um primitivo int para um objeto String com um Integer no meio
		int num2 = 100000;
		System.out.println(Integer.toString(num2));
	}

}
